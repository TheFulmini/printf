#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;
	size_t	strlen;

	strlen = ft_strlen(s);
	if (start > strlen)
		len = 0;
	if (strlen - start < len)
		len = strlen - start;
	i = 0;
	if (!(str = (char *)malloc(len + 1)))
		return (NULL);
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
